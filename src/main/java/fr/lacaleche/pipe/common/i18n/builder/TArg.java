package fr.lacaleche.pipe.common.i18n.builder;

public class TArg {

    private String key;
    private String value;

    public TArg(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

}
